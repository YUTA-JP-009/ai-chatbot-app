// 全Q&Aを1つのテキストに結合する関数
export function getAllQAAsText(): string {
  return qaDatabase.map((qa) => {
    return `${qa.id}: ${qa.question}\nA: ${qa.answer}\n参照URL: ${qa.url}`;
  }).join('\n\n');
}

// カテゴリ別にQ&Aを取得する関数
export function getQAByCategory(category: string): QAItem[] {
  return qaDatabase.filter((qa) => qa.category === category);
}

// IDでQ&Aを検索する関数
export function getQAById(id: string): QAItem | undefined {
  return qaDatabase.find((qa) => qa.id === id);
}
